public class P03_asciiTablosu {
    public static void main(String[] args) {

        // 0-255 e kadar olan harflerin , sayı ve harf değerini konsola yazdırınız.

       // ascii tablosu

        for (int i = 0; i < 255; i++) {
            System.out.println( i+"-"+ (char)i);
        }











    }
}
